import mongoose from "mongoose";
import { IBLog } from "../types/blog";

const { Schema } = mongoose;

const blogSchema = new Schema(
  {
    title: {
      type: String,
      required: true,
    },
    body: {
      type: String,
      required: true,
    },
    image: {
      type: String,
      required: true,
    },
    author: {
      type: Object,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

export const Blog = mongoose.model<IBLog>("BlogPost", blogSchema);
